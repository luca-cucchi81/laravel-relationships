    public function store(Request $request)
    {
        $data = $request->all();
        $user = Auth::id();
        $data['user_id'] = $user;
        $validator = Validator::make($data, [
           'name' => 'required|unique:categories|max:80',
           'description' => 'required|max:100',
           'user_id' => 'required',
       ]);

       if ($validator->fails()) {
           return redirect()->route('admin.categories.create')
                       ->withErrors($validator)
                       ->withInput();
